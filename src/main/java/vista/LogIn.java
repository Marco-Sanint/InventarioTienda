    public void ingresarAlSistema(){
        String cedula = txtCedulaEmpleado.getText();
        String clave = txtClave.getText();
        
        boolean confirmacion = confirmarEmpleado(cedula, clave);
        
        if (confirmacion == true){
            Menu menu = new Menu();
            menu.setVisible(true);
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Error: CÃ©dula o clave incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

